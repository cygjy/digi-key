    public static byte[] httpRequestGetBytes(String urlStr, String method, Properties requestProperty, Properties requestHeaderProperty, List<Map<String, String>> cookieList) throws IOException {
        URL url = null;
        URLConnection httpConn = null;
        if (method != null && method.equalsIgnoreCase("post")) {
            url = new URL(urlStr);
            httpConn = url.openConnection();
        } else {
            if (requestProperty != null && requestProperty.size() > 0) {
                urlStr = urlStr + "?" + getParameterStr(requestProperty);
            }
            url = new URL(urlStr);
            httpConn = url.openConnection();
        }
        if (requestHeaderProperty != null) {
            Iterator<Map.Entry<Object, Object>> entrys = requestHeaderProperty.entrySet().iterator();
            while (entrys.hasNext()) {
                Map.Entry<Object, Object> entry = entrys.next();
                String key = entry.getKey().toString();
                String value = entry.getValue().toString();
                httpConn.setRequestProperty(key, value);
            }
        }
        if (method != null && method.equalsIgnoreCase("post")) {
            httpConn.setConnectTimeout(10000);
            httpConn.setReadTimeout(10000);
            httpConn.setDoOutput(true);
            OutputStreamWriter out = new OutputStreamWriter(httpConn.getOutputStream(), "UTF-8");
            out.write(getParameterStr(requestProperty));
            out.close();
        }
        if (cookieList != null) {
            List<Map<String, String>> tmpCookieList = getCookieList(httpConn.getHeaderField("Set-Cookie"));
            if (tmpCookieList != null) {
                cookieList.addAll(tmpCookieList);
            }
        }
        return FileUtil.readBytes(httpConn.getInputStream());
    }

    private static List<Map<String, String>> getCookieList(String cookie) throws IOException {
        if (StringUtils.isNotEmpty(cookie)) {
            List<Map<String, String>> list = new ArrayList<>();
            String[] cookies = cookie.split(";");
            for (int i = 0; i < cookies.length; i++) {
                String[] thisCookie = cookies[i].split("=");
                if (thisCookie.length > 1) {
                    Map<String, String> map = new HashMap<>();
                    map.put(thisCookie[0], thisCookie[1]);
                    list.add(map);
                }
            }
            return list;
        }
        return null;
    }

    private static String getParameterStr(Properties requestProperty) throws IOException {
        if (requestProperty != null && requestProperty.size() > 0) {
            StringBuilder sb = new StringBuilder();
            Iterator<Map.Entry<Object, Object>> entrys = requestProperty.entrySet().iterator();
            while (entrys.hasNext()) {
                Map.Entry<Object, Object> entry = entrys.next();
                String key = entry.getKey().toString();
                String value = entry.getValue().toString();
                sb.append(key);
                sb.append("=");
                sb.append(URLEncoder.encode(value, "UTF-8"));
                if (entrys.hasNext()) {
                    sb.append("&");
                }
            }
            return sb.toString();
        } else {
            return "";
        }
    }

    public static String sendSSLPost(String url, String param)
    {
        StringBuilder result = new StringBuilder();
        String urlNameString = url + "?" + param;
        try
        {
            log.info("sendSSLPost - {}", urlNameString);
            SSLContext sc = SSLContext.getInstance("SSL");
            sc.init(null, new TrustManager[] { new TrustAnyTrustManager() }, new java.security.SecureRandom());
            URL console = new URL(urlNameString);
            HttpsURLConnection conn = (HttpsURLConnection) console.openConnection();
            conn.setRequestProperty("accept", "*/*");
            conn.setRequestProperty("connection", "Keep-Alive");
            conn.setRequestProperty("user-agent", "Mozilla/4.0 (compatible; MSIE 6.0; Windows NT 5.1;SV1)");
            conn.setRequestProperty("Accept-Charset", "utf-8");
            conn.setRequestProperty("contentType", "utf-8");
            conn.setDoOutput(true);
            conn.setDoInput(true);

            conn.setSSLSocketFactory(sc.getSocketFactory());
            conn.setHostnameVerifier(new TrustAnyHostnameVerifier());
            conn.connect();
            InputStream is = conn.getInputStream();
            BufferedReader br = new BufferedReader(new InputStreamReader(is));
            String ret = "";
            while ((ret = br.readLine()) != null)
            {
                if (ret != null && !"".equals(ret.trim()))
                {
                    result.append(new String(ret.getBytes(StandardCharsets.ISO_8859_1), StandardCharsets.UTF_8));
                }
            }
            log.info("recv - {}", result);
            conn.disconnect();
            br.close();
        }
        catch (ConnectException e)
        {
            log.error("调用HttpUtils.sendSSLPost ConnectException, url=" + url + ",param=" + param, e);
        }
        catch (SocketTimeoutException e)
        {
            log.error("调用HttpUtils.sendSSLPost SocketTimeoutException, url=" + url + ",param=" + param, e);
        }
        catch (IOException e)
        {
            log.error("调用HttpUtils.sendSSLPost IOException, url=" + url + ",param=" + param, e);
        }
        catch (Exception e)
        {
            log.error("调用HttpsUtil.sendSSLPost Exception, url=" + url + ",param=" + param, e);
        }
        return result.toString();
    }

    private static class TrustAnyTrustManager implements X509TrustManager
    {
        @Override
        public void checkClientTrusted(X509Certificate[] chain, String authType)
        {
        }

        @Override
        public void checkServerTrusted(X509Certificate[] chain, String authType)
        {
        }

        @Override
        public X509Certificate[] getAcceptedIssuers()
        {
            return new X509Certificate[] {};
        }
    }

    private static class TrustAnyHostnameVerifier implements HostnameVerifier
    {
        @Override
        public boolean verify(String hostname, SSLSession session)
        {
            return true;
        }
    }
